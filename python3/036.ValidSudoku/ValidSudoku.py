# O(1)
# Runtime Beats 100.00%

from typing import List


class Solution:

    def isValidSudoku(self, board: List[List[str]]) -> bool:
        rows = [set() for _ in range(9)]
        cols = [set() for _ in range(9)]
        boxes = [set() for _ in range(9)]

        for i in range(9):
            for j in range(9):
                if board[i][j] != '.':
                    b = (i // 3) * 3 + j // 3
                    if board[i][j] in rows[i] or board[i][j] in cols[
                            j] or board[i][j] in boxes[b]:
                        return False

                    rows[i].add(board[i][j])
                    cols[j].add(board[i][j])
                    boxes[b].add(board[i][j])

        return True


def test1():
    board = [["5", "3", ".", ".", "7", ".", ".", ".", "."],
             ["6", ".", ".", "1", "9", "5", ".", ".", "."],
             [".", "9", "8", ".", ".", ".", ".", "6", "."],
             ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
             ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
             ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
             [".", "6", ".", ".", ".", ".", "2", "8", "."],
             [".", ".", ".", "4", "1", "9", ".", ".", "5"],
             [".", ".", ".", ".", "8", ".", ".", "7", "9"]]
    s = Solution()
    assert s.isValidSudoku(board) == True


